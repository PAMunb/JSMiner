// Decimal Number with Separator
const decimalNumber = 1_000_000;
console.log("Decimal Number:", decimalNumber); // ouput: 1000000

// Hexadecimal Number with Separator
const hexNumber = 0xA_B_C_D_E;
console.log("Hexadecimal Number:", hexNumber); // ouput: 703710

// Octal Number with Separator
const octalNumber = 0o2_3_5_7;
console.log("Octal Number:", octalNumber); // ouput: 1263

// Binary Number with Separator
const binaryNumber = 0b1010_0101_1001;
console.log("Binary Number:", binaryNumber); // ouput: 2649